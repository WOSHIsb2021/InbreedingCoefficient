/**
 * 为母牛数组中的每一头牛计算与公牛列表的配对近交系数
 * @param {Array<Object>} cows - 母牛对象数组
 * @param {Array<Object>} bulls - 公牛对象数组
 * @returns {Array<Object>} 所有配对结果的集合
 */
function calculateForMultipleCows(cows, bulls) {
    let allResults = [];

    // 遍历母牛数组
    cows.forEach(cow => {
        // 对每一头母牛，调用原始的计算函数
        const singleCowResults = calculateBreedingInbreeding(cow, bulls);

        // 将单头母牛的计算结果合并到总结果数组中
        if (singleCowResults && singleCowResults.length > 0) {
            // 使用扩展运算符(...)可以高效地将一个数组的元素添加到另一个数组
            allResults.push(...singleCowResults);
        }
    });

    return allResults;
}


